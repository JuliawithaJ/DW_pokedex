/**
 * Récupère l'identifiant du pokémon lié à l'image selon le nom de l'image originel
 * @param String le nom de l'image
 * @return String
 */
function getPokemonId($imgFullName)
{
  // casse la chaine par '/'
  $explode_full_name = explode('/', $imgFullName);
  // récupère toujours le dernier éléments du tableau : 001.png
  $lastIndex = count($explode_full_name) - 1;

  // casse la chaine par '.'
  $explode_file_name = explode('.', $explode_full_name[$lastIndex]);
  // récupère le premier index 
  return $explode_file_name[0];
}

